    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
      
        try {
            Class.forName("org.apache.derby.jdbc.ClientDriver");
       
            Connection con= DriverManager.getConnection("jdbc:derby://localhost:1527/project","root","root");
            PreparedStatement st;  
            st = con.prepareStatement("SELECT SRNO,NAME,PHNO,BRAND FROM ADDC WHERE SRNO=?");
            String id1=id.getText();
            st.setString(1, id1);
            ResultSet rs=st.executeQuery();
            if(rs.next()==false)
            {
                JOptionPane.showMessageDialog(this,"sory data not found");
                name.setText("");
                brand.setText("");
               
            }
            else
            {
                name.setText(rs.getString("name"));
                brand.setText(rs.getString("brand"));
               
            }
            
            st.close();
            
           
            
            
            
        } catch (Exception ex) {
            System.out.println(ex);
        } 
    }//GEN-LAST:event_searchActionPerformed

